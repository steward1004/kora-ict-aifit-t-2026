const state = {
  lang: localStorage.getItem('pg-lang') || (navigator.language.startsWith('ja') ? 'ja' : navigator.language.startsWith('en') ? 'en' : 'ko'),
  theme: localStorage.getItem('pg-theme') || (window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light'),
  pixelIndex: 0
};

const $ = (selector) => document.querySelector(selector);
const $$ = (selector) => [...document.querySelectorAll(selector)];

function applyLanguage(lang) {
  state.lang = lang;
  document.documentElement.lang = lang;
  localStorage.setItem('pg-lang', lang);
  document.title = lang === 'ja' ? 'Pixel Grug' : (lang === 'en' ? 'Pixel Grug' : 'Pixel Grug');

  $$('.lang-btn').forEach((btn) => btn.classList.toggle('active', btn.dataset.lang === lang));

  const dict = I18N[lang];
  document.querySelectorAll('[data-i18n]').forEach((node) => {
    const key = node.dataset.i18n;
    if (dict[key]) node.textContent = dict[key];
  });

  const pixel = pixelSets[state.pixelIndex];
  $('#pixelName').textContent = pixel.name[lang];
  $('#pixelQuote').textContent = pixel.quote[lang];
}

function applyTheme(theme) {
  state.theme = theme;
  document.documentElement.setAttribute('data-theme', theme);
  localStorage.setItem('pg-theme', theme);
  $('#themeToggle').textContent = theme === 'dark' ? '☾' : '☀';
}

function initReveal() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) entry.target.classList.add('visible');
    });
  }, { threshold: 0.14 });

  $$('.reveal').forEach((el) => observer.observe(el));
}

function initHeaderScroll() {
  const header = $('.site-header');
  const toggleHeader = () => header.classList.toggle('scrolled', window.scrollY > 12);
  toggleHeader();
  window.addEventListener('scroll', toggleHeader);
}

function initMobileNav() {
  const toggle = $('.nav-toggle');
  const nav = $('.nav-links');
  toggle.addEventListener('click', () => {
    const isOpen = nav.classList.toggle('open');
    toggle.setAttribute('aria-expanded', String(isOpen));
  });
}

function updatePixel(index) {
  state.pixelIndex = index;
  const pixel = pixelSets[index];
  $('#pixelImage').src = pixel.image;
  $('#pixelName').textContent = pixel.name[state.lang];
  $('#pixelQuote').textContent = pixel.quote[state.lang];
}

function initDailyPixel() {
  $('#shufflePixel').addEventListener('click', () => {
    const next = (state.pixelIndex + 1) % pixelSets.length;
    updatePixel(next);
  });
}

function initThemeToggle() {
  $('#themeToggle').addEventListener('click', () => {
    const nextTheme = state.theme === 'dark' ? 'light' : 'dark';
    applyTheme(nextTheme);
  });
}

function initLanguageToggle() {
  $$('.lang-btn').forEach((button) => {
    button.addEventListener('click', () => applyLanguage(button.dataset.lang));
  });
}

applyTheme(state.theme);
applyLanguage(state.lang);
initReveal();
initHeaderScroll();
initMobileNav();
initDailyPixel();
initThemeToggle();
initLanguageToggle();

const prefersReducedMotion = window.matchMedia('(prefers-reduced-motion: reduce)').matches;
if (prefersReducedMotion) {
  $$('.reveal').forEach((el) => el.classList.add('visible'));
}
